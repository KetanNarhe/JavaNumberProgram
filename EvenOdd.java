package com.NumberPro;

public class EvenOdd {

	public static void main(String[] args) {

         int num=7;
         
         if(num%2==0)
         {
        	 System.out.println("It is a Even Number");
         }
         else
         {
        	 System.out.println("It is Odd Number");
         }

	}

}
